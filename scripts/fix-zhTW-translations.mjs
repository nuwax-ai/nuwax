function convertToTraditional(text) {
  let result = text;
  // Sort by length (longest first) to avoid partial replacements
  const sortedEntries = Object.entries(simplifiedToTraditional)
    .sort((a, b) => b[0].length - a[0].length);

  for (const [simplified, traditional] of sortedEntries) {
    result = result.replace(new RegExp(escapeRegExp(simplified), 'g'), traditional);
  }
  return result;
}

function escapeRegExp(string) {
  return string.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function extractKeys(filePath) {
  const content = fs.readFileSync(filePath, 'utf-8');
  const matches = content.matchAll(/'([^']+)':\s*'([^']*)'/g);
  const keys = {};
  for (const match of matches) {
    keys[match[1]] = match[2];
  }
  return keys;
}

function main() {
  const zhCNPath = '/Users/apple/workspace/nuwax/src/locales/i18n/zh-CN.ts';
  const zhTWPath = '/Users/apple/workspace/nuwax/src/locales/i18n/zh-TW.ts';

  console.log('Reading translation files...');
  const zhCN = extractKeys(zhCNPath);
  const zhTW = extractKeys(zhTWPath);

  const cnKeys = Object.keys(zhCN).sort();
  const twKeys = Object.keys(zhTW).sort();

  const missingKeys = cnKeys.filter(key => !twKeys.includes(key));

  console.log(`\nStatistics:`);
  console.log(`  Total keys in zh-CN: ${cnKeys.length}`);
  console.log(`  Total keys in zh-TW: ${twKeys.length}`);
  console.log(`  Missing keys in zh-TW: ${missingKeys.length}`);

  if (missingKeys.length === 0) {
    console.log('\n✓ All keys are present in zh-TW!');
    return;
  }

  console.log(`\nAdding ${missingKeys.length} missing keys to zh-TW...\n`);

  // Read the current zh-TW file
  let zhTWContent = fs.readFileSync(zhTWPath, 'utf-8');

  // Find the position before the closing brace
  const lastBraceIndex = zhTWContent.lastIndexOf('}');

  if (lastBraceIndex === -1) {
    console.error('Could not find closing brace in zh-TW.ts');
    process.exit(1);
  }

  // Generate the new entries
  const newEntries = missingKeys.map(key => {
    const cnValue = zhCN[key];
    const twValue = convertToTraditional(cnValue);
    return `  '${key}': '${twValue}',`;
  }).join('\n');

  // Insert the new entries before the closing brace
  const newContent = zhTWContent.slice(0, lastBraceIndex) +
    '\n' +
    newEntries +
    '\n' +
    zhTWContent.slice(lastBraceIndex);

  // Write back to the file
  fs.writeFileSync(zhTWPath, newContent, 'utf-8');

  console.log(`\n✓ Successfully added ${missingKeys.length} missing keys to zh-TW.ts`);
  console.log(`\nFirst 10 added keys:`);
  missingKeys.slice(0, 10).forEach(key => {
    console.log(`  ${key}: ${convertToTraditional(zhCN[key])}`);
  });

  if (missingKeys.length > 10) {
    console.log(`  ... and ${missingKeys.length - 10} more`);
  }

  // Save missing keys report
  const reportPath = '/tmp/missing_keys_report.json';
  fs.writeFileSync(reportPath, JSON.stringify({
    timestamp: new Date().toISOString(),
    totalMissing: missingKeys.length,
    missingKeys: missingKeys.map(key => ({
      key,
      zhCN: zhCN[key],
      zhTW: convertToTraditional(zhCN[key])
    }))
  }, null, 2));

  console.log(`\n✓ Detailed report saved to: ${reportPath}`);
}

main();
